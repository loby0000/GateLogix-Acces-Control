import axios from 'axios';

export default {
  data() {
    return {
      searchQuery: "",
      selectedType: "",
      records: [], // Registros del d√≠a actual
      allRecords: [], // Todos los registros hist√≥ricos
      filteredRecords: [],
      showModal: false,
      selectedUser: null,
      userHistoryRecords: [], // Registros hist√≥ricos del usuario seleccionado
      loading: false,
      error: null,
      // Paginaci√≥n
      currentPage: 1,
      recordsPerPage: 50,
      totalRecords: 0
    };
  },
  async mounted() {
    await this.cargarHistorial();
  },
  methods: {
    // üîπ Cargar historial desde el backend (solo del d√≠a actual)
    async cargarHistorial() {
      this.loading = true;
      this.error = null;
      
      try {
        const token = localStorage.getItem('token');
        if (!token) {
          this.$router.push('/login');
          return;
        }

        const response = await axios.get(
          'http://localhost:3000/api/historial/listar',
          {
            headers: { Authorization: `Bearer ${token}` },
            timeout: 10000
          }
        );

        // Procesar y formatear los datos
        const allRecords = response.data.map(record => ({
          id: record._id,
          usuario: record.usuario?.nombre || 'N/A',
          documento: record.usuario?.numeroDocumento || 'N/A',
          email: record.usuario?.email || 'N/A',
          serial: record.serial || 'N/A',
          entrada: this.formatearFecha(record.entrada),
          salida: record.salida ? this.formatearFecha(record.salida) : null,
          docGuardia: record.docGuardia || record.guardia?.documento || 'N/A',
          nombreGuardia: record.guardia?.nombre || 'N/A',
          estado: record.estado || 'N/A',
          tipo: this.determinarTipo(record),
          fechaCreacion: record.createdAt,
          fechaEntrada: new Date(record.entrada),
          usuarioId: record.usuario?._id,
          raw: record // Datos originales para referencia
        }));

        // üîπ Filtrar solo registros del d√≠a actual
        const hoy = new Date();
        hoy.setHours(0, 0, 0, 0);
        const ma√±ana = new Date(hoy);
        ma√±ana.setDate(ma√±ana.getDate() + 1);

        const registrosDelDia = allRecords.filter(record => {
          const fechaRegistro = record.fechaEntrada;
          return fechaRegistro >= hoy && fechaRegistro < ma√±ana;
        });

        // üîπ Agrupar por usuario y mostrar solo el √∫ltimo registro de cada uno
        const usuariosUnicos = new Map();
        
        registrosDelDia.forEach(record => {
          const usuarioKey = record.usuarioId || record.documento;
          const registroExistente = usuariosUnicos.get(usuarioKey);
          
          // Si no existe o el registro actual es m√°s reciente, lo reemplaza
          if (!registroExistente || new Date(record.fechaCreacion) > new Date(registroExistente.fechaCreacion)) {
            usuariosUnicos.set(usuarioKey, record);
          }
        });

        // Convertir el Map a array y ordenar por fecha m√°s reciente
        this.records = Array.from(usuariosUnicos.values())
          .sort((a, b) => new Date(b.fechaCreacion) - new Date(a.fechaCreacion));

        // Guardar todos los registros para el modal
        this.allRecords = allRecords;
        
        this.totalRecords = this.records.length;
        this.filteredRecords = [...this.records];
        
        console.log(`‚úÖ Usuarios √∫nicos del d√≠a: ${this.records.length} (de ${registrosDelDia.length} movimientos totales)`);
        console.log(`üìä Total hist√≥rico: ${allRecords.length} registros`);
        
      } catch (err) {
        console.error('‚ùå Error cargando historial:', err);
        this.error = 'Error al cargar el historial. Verifique su conexi√≥n.';
        
        if (err.response?.status === 401) {
          localStorage.removeItem('token');
          this.$router.push('/login');
        }
      } finally {
        this.loading = false;
      }
    },

    // üîπ Determinar tipo de movimiento basado en los datos
    determinarTipo(record) {
      if (record.salida) {
        return 'Salida';
      } else if (record.entrada) {
        return 'Entrada';
      }
      return 'Desconocido';
    },

    // üîπ Formatear fechas de manera legible
    formatearFecha(fecha) {
      if (!fecha) return null;
      
      try {
        const date = new Date(fecha);
        return date.toLocaleString('es-ES', {
          year: 'numeric',
          month: '2-digit',
          day: '2-digit',
          hour: '2-digit',
          minute: '2-digit',
          second: '2-digit'
        });
      } catch (err) {
        console.error('Error formateando fecha:', err);
        return fecha;
      }
    },

    // üîπ Formatear solo fecha (sin hora) para el modal
    formatearFechaSolo(fecha) {
      if (!fecha) return null;
      
      try {
        const date = new Date(fecha);
        return date.toLocaleDateString('es-ES', {
          year: 'numeric',
          month: '2-digit',
          day: '2-digit'
        });
      } catch (err) {
        console.error('Error formateando fecha:', err);
        return fecha;
      }
    },

    // üîπ Filtrar tabla con b√∫squeda mejorada
    filterTable() {
      const query = this.searchQuery.trim().toLowerCase();
      
      this.filteredRecords = this.records.filter(record => {
        // B√∫squeda por texto
        const matchesSearch = !query || [
          record.usuario,
          record.documento,
          record.serial,
          record.entrada,
          record.salida,
          record.docGuardia,
          record.nombreGuardia,
          record.email
        ].some(field => 
          field && field.toString().toLowerCase().includes(query)
        );
        
        // Filtro por tipo
        const matchesType = !this.selectedType || record.tipo === this.selectedType;
        
        return matchesSearch && matchesType;
      });
      
      console.log(`üîç Filtros aplicados: ${this.filteredRecords.length}/${this.records.length} registros`);
    },

    // üîπ Recargar historial
    async recargarHistorial() {
      await this.cargarHistorial();
    },

    // üîπ Exportar datos (placeholder)
    exportData(type) {
      if (this.filteredRecords.length === 0) {
        alert('No hay registros para exportar.');
        return;
      }
      
      // TODO: Implementar exportaci√≥n real
      alert(`Funci√≥n para exportar ${this.filteredRecords.length} registros como ${type.toUpperCase()} ser√° implementada pr√≥ximamente.`);
    },

    // üîπ Navegaci√≥n
    goToDashboard() {
      this.$router.push({ path: '/dashboard' });
    },

    // üîπ Modal de usuario - Mostrar historial completo
    showUserModal(record) {
      this.selectedUser = record;
      
      // Filtrar todos los registros hist√≥ricos del usuario
      this.userHistoryRecords = this.allRecords.filter(r => 
        r.usuarioId === record.usuarioId || 
        (r.documento === record.documento && r.documento !== 'N/A')
      ).sort((a, b) => new Date(b.fechaCreacion) - new Date(a.fechaCreacion));
      
      console.log(`üìã Mostrando historial de ${record.usuario}: ${this.userHistoryRecords.length} registros`);
      this.showModal = true;
    },

    closeUserModal() {
      this.showModal = false;
      this.selectedUser = null;
      this.userHistoryRecords = [];
    },
  }
}
</script>

